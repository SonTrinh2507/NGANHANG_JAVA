    private void showData() {
        new LoadData();
        tableKH.setRowCount(0);
        totalRows = controller.arrayListKhachHang.size();
        totalPages = (int) Math.ceil((double) totalRows / pageSize);
        int startIndex = (currentPage - 1) * pageSize; // Chỉ mục bắt đầu của dữ liệu trên trang hiện tại
        int endIndex = Math.min(startIndex + pageSize, totalRows); // Chỉ mục kết thúc của dữ liệu trên trang hiện tại

        int dem = 0;
        for (int i = startIndex; i < endIndex; i++) {
            dem++;
            KHACHHANG kh = controller.arrayListKhachHang.get(i);
            tableKH.addRow(new Object[]{dem, kh.getCMND(), kh.getHO(), kh.getTEN(), kh.getDIACHI(), kh.getPHAI(), kh.getNGAYCAP(), kh.getSODT(), kh.getMACN()});
        }

    }

