    private void btnLUUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLUUActionPerformed

        String PHAI = (String) cbbPHAI.getSelectedItem();
        java.util.Date ngayCapUtil = dateNGAYCAP.getDate();
        java.sql.Date ngayCapSql = new java.sql.Date(ngayCapUtil.getTime());
        if (txtCMND.getText().equals("")) {
            KhachHangDao.exec_KhachHang(txtCMND.getText(), txtHO.getText(), txtTEN.getText(), txtDIACHI.getText(), PHAI, ngayCapSql, txtSODT.getText(), txtMACN.getText());
            int check = KhachHangDao.getCheck();
            if (check == 0) {
                KhachHangDao.exec_MoTaiKhoan(txtCMND.getText(), txtMACN.getText());
            }
        } else {
            KHACHHANG kh = new KHACHHANG();
            kh.setHO(txtHO.getText());
            kh.setTEN(txtTEN.getText());
            kh.setDIACHI(txtDIACHI.getText());
            kh.setPHAI(PHAI);
            kh.setNGAYCAP(ngayCapSql);
            kh.setSODT(txtSODT.getText());
            KhachHangDao.updateKhachHang(kh, txtCMND.getText());
        }

        btnSUA.setEnabled(false);
        btnLUU.setEnabled(false);
        btnXOA.setEnabled(false);
        btnRESET.setEnabled(true);
        btnTHEM.setEnabled(true);
        showData();
// TODO add your handling code here:
    }//GEN-LAST:event_btnLUUActionPerformed

