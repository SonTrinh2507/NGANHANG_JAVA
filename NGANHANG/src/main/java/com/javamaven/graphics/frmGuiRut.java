    private void btnGUITIENActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGUITIENActionPerformed
        String LOAIGD = (String) cbbLOAIGD.getSelectedItem();
        long SOTIEN = Long.parseLong(txtSOTIEN.getText());
        GiaoDichDao.exec_GDGuiRut(txtSOTK.getText(), LOAIGD, SOTIEN, txtMANV.getText());
        int check = GiaoDichDao.getCheck();
        switch (check) {
             case 0 ->
                JOptionPane.showMessageDialog(null, "Gửi tiền thành công");
            case 1 ->
                JOptionPane.showMessageDialog(null, "SOTK không tồn tại");
            case 2 ->
                JOptionPane.showMessageDialog(null, "Gửi tiền thất bại");
           
        }
         btnGUITIEN.setEnabled(false);
        String s = "";
        txtSOTIEN.setText(s);
        txtSOTK.setText(s);
        cbbLOAIGD.setSelectedItem(null);
        dateGD.setDate(null);
        txtMANV.setText(s);
        btnRUTTIEN.setEnabled(false);
        txtMANV.setEnabled(false);
        txtSOTIEN.setEnabled(false);
        txtSOTK.setEnabled(false);
        dateGD.setEnabled(false);
        cbbLOAIGD.setEnabled(false);
        cbbLOAIGD.setSelectedItem(null); 
        showData();

// TODO add your handling code here:
    }//GEN-LAST:event_btnGUITIENActionPerformed

