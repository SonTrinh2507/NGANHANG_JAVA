    private void cbbLOAIGDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbLOAIGDItemStateChanged
        if (cbbLOAIGD.getSelectedItem() != null) {
            String CHON = (String) cbbLOAIGD.getSelectedItem();

            if (CHON.equals("GT")) {
                btnGUITIEN.setEnabled(true);
                btnRUTTIEN.setEnabled(false);
            } else if (CHON.equals("RT")) {
                btnRUTTIEN.setEnabled(true);
                btnGUITIEN.setEnabled(false);
            } else {
                System.err.println("HẾT");
            }
        }// TODO add your handling code here: // TODO add your handling code here:
    }//GEN-LAST:event_cbbLOAIGDItemStateChanged

