    private void showData() {
        new LoadData();
        tableGuiRut.setRowCount(0);
        int dem = 1;
        for (GD_GUIRUT gr : controller.arrayListGuiRut) {
            tableGuiRut.addRow(new Object[]{dem++, gr.getMAGD(), gr.getSOTK(), gr.getLOAIGD(), gr.getNGAYGD(), gr.getSOTIEN(), gr.getMANV()});
        }

    }

